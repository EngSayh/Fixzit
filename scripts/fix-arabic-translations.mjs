/**
 * Normalize English text for matching
 * @param {string} text - Input text
 * @returns {string} - Normalized text
 */
function normalizeKey(text) {
  return text
    .replace(/\[AR\]\s*/g, '')
    .trim()
    .toLowerCase()
    .replace(/\s+/g, ' ');
}

/**
 * Find best Arabic translation for an English phrase
 * @param {string} englishText - Original English text
 * @returns {string|null} - Arabic translation or null
 */
function findTranslation(englishText) {
  const cleaned = englishText.replace(/\[AR\]\s*/g, '').trim();
  
  // Direct match
  if (TRANSLATION_DICTIONARY[cleaned]) {
    return TRANSLATION_DICTIONARY[cleaned];
  }
  
  // Case-insensitive match
  const lowerCleaned = cleaned.toLowerCase();
  for (const [key, value] of Object.entries(TRANSLATION_DICTIONARY)) {
    if (key.toLowerCase() === lowerCleaned) {
      return value;
    }
  }
  
  // Partial match for phrases
  for (const [key, value] of Object.entries(TRANSLATION_DICTIONARY)) {
    if (lowerCleaned.includes(key.toLowerCase()) || key.toLowerCase().includes(lowerCleaned)) {
      // Weight by match quality
      if (key.toLowerCase() === lowerCleaned) return value;
    }
  }
  
  return null;
}

/**
 * Process JSON object recursively and replace [AR] placeholders
 * @param {object} obj - JSON object to process
 * @param {object} stats - Statistics object
 * @returns {object} - Processed object
 */
function processObject(obj, stats) {
  if (typeof obj === 'string') {
    if (obj.includes('[AR]')) {
      stats.found++;
      const translation = findTranslation(obj);
      if (translation) {
        stats.translated++;
        return translation;
      } else {
        stats.notFound.push(obj);
        return obj; // Keep original if no translation found
      }
    }
    return obj;
  }
  
  if (Array.isArray(obj)) {
    return obj.map(item => processObject(item, stats));
  }
  
  if (typeof obj === 'object' && obj !== null) {
    const result = {};
    for (const [key, value] of Object.entries(obj)) {
      result[key] = processObject(value, stats);
    }
    return result;
  }
  
  return obj;
}

